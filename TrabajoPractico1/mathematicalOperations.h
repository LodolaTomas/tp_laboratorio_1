/** \brief  Realiza una suma de dos numeros y guarda el resultado con un puntero
 *
 * \param float primer numero ingresado
 * \param float segundo numero ingresado
 * \param float* direccion pasada para guardar el resultado
 * \return int retorna 1 si se sumo correctamente
 *
 */
int sumador(float, float, float* );


/** \brief Realiza una divicion entre dos numero y guarda el resultado con un puntero
 *
 * \param float el primer numero que recibe como parametro
 * \param float el segundo numero que recibe como parametro
 * \param float* apunta a una variable que va a guardar el resultado
 * \return int retorna 1 si resto correctamente y 0 si no se logro hacer la divicion
 *
 */
int divicion(float, float, float*);

/** \brief Calcula el factorial de un numero y lo guarda con puntero
 *
 * \param float el numero que quiero factorizar
 * \param int*  apunta a una variable que va a guardar el dato
 * \return int retorna 1 si lo factorizo correctamene
 *
 */
int factorial(float,int*);

/** \brief Realiza una resta de dos numero y guarda el resultado con un punteo
 *
 * \param float Recibe el primer numero ingresado
 * \param float Recibe el segundo numero ingresado
 * \param float* apunta a una variable que va a guardar el resultado
 * \return int retorna 1 si se resto correctemente
 *
 */
int restador(float, float,float*);

/** \brief multiplica dos numeros y guarda el resultado en una variable utiizando puntero
 *
 * \param float Primer numero ingresado para multiplicar
 * \param float Segundo numero ingresado
 * \param float* apunta a una variable que va a guardar el resultado
 * \return int
 *
 */
int multiplicador(float, float, float*);

